
public class Main {
	
	public static void main(String [] args) {
		
		int inteiro = 10;
		float reais = 10.5f;
		boolean aberto = false;
		char letra = 'A';
		String palavra = "Varios caracteres";
		
		System.out.println("Valor do tipo inteiro: " + inteiro);
		System.out.println("Valor do tipo real/float: " + reais);
		System.out.println("Valor do tipo logico/boolean: " + aberto);
		System.out.println("Valor do tipo caracter: " + letra);
		System.out.println("Valor do tipo String/cadeia: " + palavra);
		
		
	}

}
